import solveFarm from '../B.js';

describe('2006 -> Fase 2 -> Lobo Mau', () => {

    test('Teste 1', () => {
        const matrix = [
                        [ '.', '.', '.', '#', '.', '.' ],
                        [ '.', '#', '#', 'v', '#', '.' ],
                        [ '#', 'v', '.', '#', '.', '#' ],
                        [ '#', '.', 'k', '#', '.', '#' ],
                        [ '.', '#', '#', '#', '.', '#' ],
                        [ '.', '.', '.', '#', '#', '#' ]
                    ]

        const result = solveFarm(matrix);

        expect(result).toEqual([0, 2]);
    });

    test('Teste 2', () => {
        const matrix = [['.', '#', '#','#', '#', '#','#', '.'],
                        ['#', '.', '.', 'k', '.', '.','.', '#'],
                        ['#', '.', '#','#', '#', '#','.', '#'],
                        ['#', '.', '#','v', '.', '#','.', '#'],
                        ['#', '.', '#','.', 'k', '#','k', '#'],
                        ['#', 'k', '.','#', '#', '.','.', '#'],
                        ['#', '.', 'v','.', '.', 'v','.', '#'],
                        ['.', '#', '#','#', '#', '#','#', '.']
                    ]

        const result = solveFarm(matrix);

        expect(result).toEqual([3, 1]);
    });

    test('Teste 3', () => {
        const matrix = [
            [
                '.', '#', '#', '#',
                '.', '#', '#', '#',
                '#', '#', '.', '.'
            ],
            [
                '#', '.', 'k', 'k',
                '#', '.', '.', '.',
                '#', 'v', '#', '.'
            ],
            [
                '#', '.', '.', 'k',
                '#', '.', '#', '.',
                '#', '.', '#', '.'
            ],
            [
                '#', '.', '.', '#',
                '#', 'k', '#', '.',
                '.', '.', '#', '.'
            ],
            [
                '#', '.', '#', 'v',
                '#', 'k', '#', '#',
                '#', '.', '#', '.'
            ],
            [
                '#', '.', '.', '#',
                'v', '#', '.', '.',
                '.', '.', '#', '.'
            ],
            [
                '#', '.', '.', '.',
                'v', '#', 'v', '#',
                '#', '#', '#', '.'
            ],
            [
                '.', '#', '#', '#',
                '#', '.', '#', 'v',
                'v', '.', 'k', '#'
            ],
            [
                '.', '.', '.', '.',
                '.', '.', '.', '#',
                '#', '#', '#'
            ]
        ]

        const result = solveFarm(matrix);

        expect(result).toEqual([3, 5]);
    });

});